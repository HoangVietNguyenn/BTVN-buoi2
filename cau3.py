# Tạo một mảng (list) có tên là my_array
my_array = [1, 2, 3, 4, 5]

# In ra mảng original array
print("Original Array:", my_array)

# Thêm một giá trị mới vào mảng 
new_value = 6
#Thêm giá trị của biến new_value vào cuối mảng my_array bằng cách sử dụng phương thức append()
my_array.append(new_value)

# in ra mảng mới sau khi thêm 
print("Array after appending", new_value, ":", my_array)
