# Tạo một mảng (list) có tên là my_array
my_array = [1, 2, 3, 4, 5]

# In ra các phần tử của mảng
print("Các phần tử của mảng:")
# vòng lặp for element in my_array để duyệt các phần tử có trong mảng và lưu vào biến element
for element in my_array:
    # in các phần tử lưu trong biến element đó ra màng hình 
    print(element)